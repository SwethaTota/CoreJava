package com.Java;

public class RelationalOperators {

	public static void main(String[] args) {
		int a = 100;
		int b = 150;
		System.out.println("is value of a is greater than b?" +(a>b));
		System.out.println("is value of a is less than b?" +(a<b));
		System.out.println("is value of a is equal to b?" +(a==b));
		System.out.println("is value of a is not equal to b?" +(a!=b));
		
		System.out.println("is value of a is greater than or equal to b?" +(a>=b));
		System.out.println("is value of b is less than or equal to b?" +(b<=a));
		System.out.println("value of a after equating: " +(a=b));

	}

}
