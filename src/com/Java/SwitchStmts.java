package com.Java;

public class SwitchStmts {

	public static void main(String[] args) {
		int x = 10;
		
		switch (x) {
		case 1:System.out.println("value of x is 1");
			
			break;
		case 2:System.out.println("value of x is 3");
		
		break;
		case 3:System.out.println("value of x is 5");
		
		break;
		default:System.out.println("value of x not found");
		break;
			
		}

	}

}
